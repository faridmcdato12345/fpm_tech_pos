<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/vue3';

const props = defineProps({
    href: {
        type: String,
        required: true,
    },
    active: {
        type: Boolean,
    },
});

const classes = computed(() =>
    props.active
        ? 'items-center px-1 pt-1 text-md font-medium leading-5 dark:bg-teal-700 text-gray-900 dark:text-gray-900 focus:outline-none focus:border-indigo-700 transition duration-150 ease-in-out'
        : 'items-center px-1 pt-1 border-transparent text-md font-medium leading-5 text-gray-900 dark:text-gray-900 hover:text-gray-700 dark:hover:text-gray-300 hover:border-gray-300 dark:hover:border-gray-700 focus:outline-none focus:text-gray-700 dark:focus:text-gray-300 focus:border-gray-300 dark:focus:border-gray-700 transition duration-150 ease-in-out'
);
</script>

<template>
    <Link :href="href" :class="classes">
    <slot />
    </Link>
</template>
